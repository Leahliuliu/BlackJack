//创建BUTTON对象
export function Btn(img, x, y, w, h) {
  this.img = img;
  this.x = x;
  this.y = y;
  this.w = w;
  this.h = h;
  this.disabled = true;
  this.anim = false;
}

//创建CARD对象
export function Card(img, x, y, w, h) {
  this.img = img;
  this.x = x;
  this.y = y;
  this.w = w;
  this.h = h;
  this.anim = false;
}

